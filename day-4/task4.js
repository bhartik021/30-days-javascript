/*
4/4 Parameters and Return Values

Explanation: Functions can accept parameters, which are values passed to the function when it is called. Functions can also return values using the return keyword.
*/

//parameters
function add(a, b) {
    return a + b;
}
console.log(add(10, 20));

// Return Values
function multiply(c, d) {
    return c * d;
}
let result = multiply(4, 2);
console.log(result);