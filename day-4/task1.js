/*
1/4 Function Declaration

Explanation: A function declaration defines a named function that can be called later in the code. It consists of the function keyword, followed by the function name, a list of parameters (enclosed in parentheses), and the function body (enclosed in curly braces).

Syntax:
function functionName(parameters) {
  // code to be executed
}
*/

function greet(name) {
    return `Hello, ${name}!`;
}
console.log(greet('Alice'));

