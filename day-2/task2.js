// 2/3 Data Types (String, Number, Boolean, Null, Undefined, Symbol)

// Explanation: JavaScript has dynamic typing, meaning variables can hold values of any type without type enforcement.

// Primitive Data Types:
// - String: Represents text. Enclosed in quotes.
// - Number: Represents both integer and floating-point numbers.
// - Boolean: Represents true or false.
// - Null: Represents the intentional absence of any object value.
// - Undefined: Indicates that a variable has not been assigned a value.
// - Symbol: A unique and immutable primitive value, used as the key for object properties.

let name = "bharti";
console.log(name);

let num = 10;
console.log(num);

let check = false;
console.log(check);

let car = null;
console.log(car);

let house;
console.log(house);

let uniqueId = Symbol('id');
console.log(uniqueId);