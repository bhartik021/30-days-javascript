// 1/3 Variables (let, const, var)

// Explanation: Variables are used to store data values. In JavaScript, you can declare a variable using var, let, or const.

// - var: Declares a variable, optionally initializing it to a value. It's function-scoped.
// - let: Declares a block-scoped variable, optionally initializing it to a value.
// - const: Declares a block-scoped, read-only named constant.

// Usage Scenarios:
// - var: Use var when you need function-scoped variables.
// - let: Use let for variables that will change and are limited to block scope.
// - const: Use const for variables that should not change after the assignment.


// var a = 10;
// console.log(a);

let b = 10;
console.log(b);

const c = 100;
console.log(c);






